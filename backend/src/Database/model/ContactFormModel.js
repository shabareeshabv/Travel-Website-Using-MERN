import mongoose, { mongo } from "mongoose";
const ContactFormSchema=new mongoose.Schema({
    name: {
        type: String,
        required: true
    },
    email: {
        type: String,
        required: true
    },
    subject: {
        type: String,
        required: true
    },
    message: {
        type: String,
        required: true
    },
    createdAt: {
        type: Date,
        default: Date.now
    }
});
const ContactForm=mongoose.model("ContactForm",ContactFormSchema)
export default ContactForm;
